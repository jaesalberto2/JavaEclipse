/*
 * Programa 6: Imprime diferentes tipos de variables
 */
package basicos;

public class ImprimeVariable {

	public static void main(String[] args) {
		//tipo + nombre + valor
		int valor=0;
		double valor2=0.0;
		String valor3=null;
		boolean valor4=false;
		System.out.println("Valor de la variable entera = "+valor);
		System.out.println("Valor de la variable flotante = "+valor2);
		System.out.println("Valor de la variable referencia = "+valor3);
		System.out.println("Valor de la variable booleana = "+valor4);

	}

}
