package basicos;

public class ConversorPesetasAEuros {

	public static void main(String[] args) {
		double euros=0.06; 
		int pesetas=10;
		System.out.println("______________________________");
		System.out.println("Conversor de pesetas a euros");
		System.out.println("______________________________");
		System.out.println("pesetas\t\teuros");
		System.out.println("______________________________");
		System.out.println(pesetas+"\t\t"+euros);
		System.out.println("______________________________");
		System.out.println((2*pesetas)+"\t\t"+(2*euros));
		System.out.println("______________________________");
		System.out.println((3*pesetas)+"\t\t"+(3*euros));
		System.out.println("______________________________");
		System.out.println((4*pesetas)+"\t\t"+(4*euros));
		System.out.println("______________________________");
		System.out.println((5*pesetas)+"\t\t"+(5*euros));
		System.out.println("______________________________");
		System.out.println((6*pesetas)+"\t\t"+(6*euros));
		System.out.println("______________________________");
		System.out.println((7*pesetas)+"\t\t"+(7*euros));
		System.out.println("______________________________");
		System.out.println((8*pesetas)+"\t\t"+(8*euros));
		System.out.println("______________________________");

	}

}
