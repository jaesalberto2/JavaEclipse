package basicos;

public class VariablesEnteras {

	public static void main(String[] args) {
		int x;  //se declara la variable x como entera
		x = 5; //se le asigna el valor 5 a la variable x
		System.out.println("El valor actual de la variable  es "+x);
		x = 7; //se le asigna el valor 7 a la variable x
		System.out.println("Y ahora es  "+x);
		


	}

}
