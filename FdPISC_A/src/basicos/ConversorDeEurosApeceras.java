package basicos;

public class ConversorDeEurosApeceras {

	public static void main(String[] args) {
		int euros=1, pesetas=166;
		System.out.println("______________________________");
		System.out.println("Conversor de euros a pesetas");
		System.out.println("______________________________");
		System.out.println("Euros\t\tPesetas");
		System.out.println("______________________________");
		System.out.println(euros+"\t\t"+pesetas);
		System.out.println("______________________________");
		System.out.println((2*euros)+"\t\t"+(2*pesetas));
		System.out.println("______________________________");
		System.out.println((3*euros)+"\t\t"+(3*pesetas));
		System.out.println("______________________________");
		System.out.println((4*euros)+"\t\t"+(4*pesetas));
		System.out.println("______________________________");
		System.out.println((5*euros)+"\t\t"+(5*pesetas));
		System.out.println("______________________________");
		System.out.println((6*euros)+"\t\t"+(6*pesetas));
		System.out.println("______________________________");
		System.out.println((7*euros)+"\t\t"+(7*pesetas));
		System.out.println("______________________________");
		System.out.println((8*euros)+"\t\t"+(8*pesetas));
		System.out.println("______________________________");
	}

}
