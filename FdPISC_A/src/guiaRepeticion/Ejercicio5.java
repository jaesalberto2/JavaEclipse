/*
 * Ejercicio 5Muestra los números del 320 al 160,
 *  contando de 20 en 20 hacia atrásutilizando un bucle while.
 */
package guiaRepeticion;

public class Ejercicio5 {

	public static void main(String[] args) {
		// Declaracion de variables
		int i=320;
		while(i>=160)
		{
		System.out.print(i+" ");
		i-=5;
		}

	}

}
