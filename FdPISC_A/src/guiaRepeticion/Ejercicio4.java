/*
 * Ejercicio 4 Muestra los números del 320 al 160, 
 * contando de 20 en 20 hacia atrásutilizando un bucle for.
 */

package guiaRepeticion;

public class Ejercicio4 {

	public static void main(String[] args) {
		// Declaracion de variables
		for(int i=320 ;i>=160;i-=5) {
			System.out.print(i+" ");
		}

	}

}
