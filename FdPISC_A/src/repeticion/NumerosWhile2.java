/*
 * Ejercicio 12: Muestra los primeros 10 numeros 
 * enteros mediante la sentencia while
 */
package repeticion;

public class NumerosWhile2 {

	public static void main(String[] args) {
		//Declaracion de variables
		int i=10;
		while (i>=1)
		{
			System.out.print(i+"\t");
			i--;
		}//fin del while

	}

}
