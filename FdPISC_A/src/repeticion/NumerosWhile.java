/*
 * Ejercicio 11: Muestra los primeros 10 numeros 
 * enteros mediante la sentencia while
 */
package repeticion;

public class NumerosWhile {

	public static void main(String[] args) {
		//Declaracion de variables
		int i=1;
		while (i<=10)
		{
			System.out.print(i+"\t");
			i++;
		}//fin del while
	}

}
